#include "includes/ft_printf.h"

int		main(void)
{
	ft_printf("%%+10.5d [%+10.5d]\n", 123);
	printf("%%+10.5d [%+10.5d]\n", 123);

	ft_printf("%%+10.5d [%+10.5d]\n", -123);
	printf("%%+10.5d [%+10.5d]\n", -123);
	
	ft_printf("%%03.2d [%03.2d]\n", -1);
	printf("%%03.2d [%03.2d]\n", -1);

	ft_printf("%%03.2d [%03.2d]\n", 1);
	printf("%%03.2d [%03.2d]\n", 1);

	ft_printf("%%+03d [%+03d]\n", 1);
	printf("%%+03d [%+03d]\n", 1);

	ft_printf("%% 03d [% 03.2d]\n", 1);
	printf("%% 03d [% 03.2d]\n", 1);
	
	ft_printf("%%+03d [%+03d]\n", -1);
	printf("%%+03d [%+03d]\n", -1);

	ft_printf("%%3d [%3d]\n", -1);
	printf("%%3d [%3d]\n", -1);
	return (0);
}
